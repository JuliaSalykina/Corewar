#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char a;

	if (n == MIN_INT)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	a = (n % 10) + 48;
	write(fd, &a, 1);
}
